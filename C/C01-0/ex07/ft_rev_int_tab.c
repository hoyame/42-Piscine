void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	d;

	i = 0;
	while (i < (size / 2))
	{
		d = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = d;
		i++;
	}
}
